final zeniqSwapRouterContract = DeployedContract(
  ContractAbi.fromJson(_abi, "ZeniqSwapRouter"),
  EthereumAddress.fromHex("0x7963c1bd24E4511A0b14bf148F93e2556AFe3C27"),
);

class ZeniqSwapRouter extends ZeniqSmartChainContract {
  ZeniqSwapRouter({required super.client})
      : super(
          contract: zeniqSwapRouterContract,
        );

  Future<BigInt> getAmountOut({
    required BigInt amountIn,
    required BigInt reserveIn,
    required BigInt reserveOut,
  }) async {
    final function = self.functions[6];

    final result =
        await read(function, [amountIn, reserveIn, reserveOut], null);

    return result[0] as BigInt;
  }

  Future<List<BigInt>> getAmountsOut({
    required BigInt amountIn,
    required List<String> path,
  }) async {
    final function = self.functions[8];

    final result = await read(
        function,
        [
          amountIn,
          [
            for (final p in path) EthereumAddress.fromHex(p),
          ]
        ],
        null);

    return [for (final r in result[0]) r as BigInt];
  }

  Future<BigInt> getAmountIn({
    required BigInt amountOut,
    required BigInt reserveIn,
    required BigInt reserveOut,
  }) async {
    final function = self.functions[5];

    final result =
        await read(function, [amountOut, reserveIn, reserveOut], null);

    return result[0] as BigInt;
  }

  Future<String> swapExactEthForTokens({
    required BigInt amountIn,
    required BigInt amountOutMin,
    required List<String> path,
    required String to,
    required BigInt deadline,
    required Credentials credentials,
  }) async {
    final function = self.functions[17];

    final result = await write(
      credentials,
      Transaction(value: EtherAmount.fromBigInt(EtherUnit.wei, amountIn)),
      function,
      [
        amountOutMin,
        [
